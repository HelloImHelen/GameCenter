Друг наших образовательных программ Михаил Пименов,  CEO компании Wonder Games и Team Lead инди-проекта "Guard of Wonderland" сам не раз задававший себе эти вопросы, создал для себя выборку всевозможных материалов по индустрии геймдева. От полезных ресурсов, до сообществ, подкастов, книг и фильмов, которые призваны помочь понять, с чего начинается и чем заканчивается разработка игрового проекта. Объединив наши силы, мы с Мишей сделали эту статью с подборкой полезных материалов по игровой индустрии. Вы найдете подборку под катом.
Чтобы объединить все накопленные материалы в одну структуру, мы решили разделить Статью на несколько разделов. <br>Во «введении» вы найдете материалы для начинающих геймдевелоперов. В разделе «Разработка» — ресурсы с бесплатным контентом. И далее каждый из подпунктов посвящен своей тематике. Несложно догадаться, что «геймдизайн» содержит в себе необходимую информацию об аспектах геймдизайна, понимание которых необходимо для создания игры. В свою очередь «Маркетинг и аналитика» содержит всё о продвижении проектов: метрики, статистические данные, расчеты стоимости проектов и т.д. Далее будут разделы о сообществах в социальных сетях, книгах, подкастах, фильмах, полезных ресурсах и конференциях.
Все эти материалы призваны помочь разобраться в устройстве игровой индустрии и ответить на большинство вопросов, которые часто возникают не только у начинающих геймдевелоперов, но и уже тертых калачей игровой индустрии. </p>
